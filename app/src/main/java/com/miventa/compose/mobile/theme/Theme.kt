@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit,
) {
    val colorScheme = getColorScheme(darkTheme, dynamicColor)
    SetupWindowAppearance(darkTheme, colorScheme)
    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        content = content,
    )
}

@Composable
private fun getColorScheme(
    darkTheme: Boolean,
    dynamicColorEnabled: Boolean,
): ColorScheme =
    when {
        dynamicColorEnabled && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }

@Suppress("DEPRECATION")
@Composable
private fun SetupWindowAppearance(
    darkTheme: Boolean,
    colorScheme: ColorScheme,
) {
    val view = LocalView.current
    val backgroundColor = colorScheme.background.toArgb()
    if (!view.isInEditMode) {
        SideEffect {
            val windowCurrent = (view.context as? Activity)?.window
            windowCurrent?.let { window ->
                window.statusBarColor = backgroundColor
                window.navigationBarColor = backgroundColor
                WindowCompat.getInsetsController(window, view).apply {
                    isAppearanceLightStatusBars = !darkTheme
                    isAppearanceLightNavigationBars = !darkTheme
                }
            }
        }
    }
}

@Composable
fun Screen(content: @Composable () -> Unit) {
    AppTheme {
        Surface(
            modifier = Modifier.safeDrawingPadding(),
            color = MaterialTheme.colorScheme.background,
        ) {
            content()
        }
    }
}
